#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;
	int		i;

	i = 0;
	p = (char *)s;
	while (p[i] != (char)c)
	{
		if (p[i] == '\0')
			return (NULL);
		i++;
	}
	return (p + i);
}
